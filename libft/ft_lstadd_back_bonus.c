#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (lst == NULL)
		return ;
	if (*lst == NULL)
	{
		if (new != NULL)
			*lst = new;
		return ;
	}
	else
		ft_lstlast(*lst)-> next = new;
}
